#ifndef LITOREYA_H
#define LITOREYA_H

#include <string>

// Соответствует функции из litoreya.cpp:
// extern "C" string process_litoreya(const string& text);
//
// "Простая литорея" - меняет местами согласные буквы русского и
// английского алфавитов внутри своего набора (первая с последней,
// вторая с предпоследней и т.д.). Гласные, цифры, пробелы и прочие
// символы не изменяются.
//
// Шифр является симметричным (повторное применение функции к
// результату возвращает исходный текст), поэтому отдельной операции
// дешифрования не требуется - encrypt и decrypt делают одно и то же.
//
// Ключ не требуется (бесключевой шифр).
typedef std::string (*process_litoreya_t)(const std::string& text);

#endif // LITOREYA_H
